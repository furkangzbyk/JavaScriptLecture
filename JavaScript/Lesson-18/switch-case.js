
let sayi = prompt("Lütfen 1 ile 5 arasında sayı giriniz");

switch (sayi) { // "1" "2" "3" "4" "5"
    case "1":
        console.log("Birini Sayıdır.");
        break;

    case "2":
        console.log("İkinci Sayıdır");
        break;

    case "3":
        console.log("Üçüncü Sayıdır");
        break;

    case "4":
        console.log("Dördüncü Sayıdır");
        break;

    case "5":
        console.log("Beşinci Sayıdır");
        break;

    default:
        console.log("Girilen sayı 1 ila 5 arasında olmalıdır...")
        break;











}

